    ts_i = ts_i[m]

    k = np.floor(ts_t_rel / dt).astype(int)
    valid = (k >= 0) & (k < x_test.size) & (ts_i >= 0) & (ts_i < p.n_ts)
    if not np.any(valid):
        print("No valid TS spikes after time-to-index mapping")
        return

